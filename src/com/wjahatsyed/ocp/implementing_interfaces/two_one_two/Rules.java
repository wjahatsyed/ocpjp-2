public class Rules {
    /*
    A class can implement more than one interface. It's perfectly legal to say, for example, the following:

    public class Ball implements Bounceable, Serializable, Runnable {...}

    1. You can extend only one class, but you can implement many interfaces(which of Java 8, means a form of
    multiple inheritance)

    In other words, subclassing defines who and what you are, whereas implementing an interface defines a role you can play
    or a hat you can wear, despite how different you might be from some other class implementing the same interface (but from a different
    inheritance tree).

    For example, a Person extends HumanBeing (although for some, that's debatable). But a Person may also implement Programmer,
    Snowboader, Employee, Parent, or PersonCrazyEnoughToTakeThisExam.

    2. An interface can itself and only extend another interface. It cannot implement an interface or extend a class.
    The first concrete class implementing te interface shall provide the implementation of all abstract methods
    in the interface hierarchy.

    A class is not allowed to extend more than one class but an interface can extend multiple interfaces




     */
}
